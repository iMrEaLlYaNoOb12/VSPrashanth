package com.vastpro.basicclose.main;

public class PrintDefaultPrimitives {
	static byte byteDefault;
	static short shortDefault;
	static int intDefault;
	static long longDefault;
	static float floatDefault;
	static double doubleDefault;
	static char charDefault;
	static boolean booleanDefault;

	public static void main(String[] args) {
		System.out.println("Default value of byte is : " + byteDefault);
		System.out.println("Default value of short is : " + shortDefault);
		System.out.println("Default value of int is : " + intDefault);
		System.out.println("Default value of long is : " + longDefault);
		System.out.println("Default value of float is : " + floatDefault);
		System.out.println("Default value of double is : " + doubleDefault);
		System.out.println("Default value of char is : " + charDefault);
		System.out.println("Default value of boolean is : " + booleanDefault);

	}

}
